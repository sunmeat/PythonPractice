# Задание 4

# Напишите программу, вычисляющую площадь треугольника.
# Пользователь с клавиатуры вводит размер основания треугольника и размер высоты.

# Считываем размер основания и высоту с клавиатуры
base = float(input("Введите размер основания треугольника: "))
height = float(input("Введите размер высоты треугольника: "))

# Вычисляем площадь треугольника
area = (base * height) / 2

# Выводим результат на экран
print("Площадь треугольника:", area)

