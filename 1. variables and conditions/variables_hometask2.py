# Задание 3

# Пользователь вводит с клавиатуры количество метров.
# Требуется вывести результат перевода метров в сантиметры,
# дециметры, миллиметры, мили.

# Считываем количество метров с клавиатуры
meters = float(input("Введите количество метров: "))

# Вычисляем перевод в сантиметры, дециметры, миллиметры и мили
centimeters = meters * 100
decimeters = meters * 10
millimeters = meters * 1000
miles = meters / 1609.34

# Выводим результат на экран
print("Сантиметры:", centimeters)
print("Дециметры:", decimeters)
print("Миллиметры:", millimeters)
print("Мили:", miles)

