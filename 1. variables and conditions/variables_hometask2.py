# Задание 2

# Пользователь вводит с клавиатуры число, состоящее из четырех цифр.
# Требуется найти произведение цифр. Например, если с клавиатуры введено 1324,
# тогда результат произведения 1*3*2*4 = 24.

# Считываем число с клавиатуры
number = int(input("Введите четырехзначное число: "))

# Вычленяем цифры из числа
digit1 = number // 1000  # Первая цифра (тысячи)
digit2 = (number // 100) % 10  # Вторая цифра (сотни)
digit3 = (number // 10) % 10  # Третья цифра (десятки)
digit4 = number % 10  # Четвертая цифра (единицы)

# Вычисляем произведение цифр
product = digit1 * digit2 * digit3 * digit4

# Выводим результат на экран
print("Произведение цифр:", product)

