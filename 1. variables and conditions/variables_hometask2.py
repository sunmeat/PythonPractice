#Задание 1

# Пользователь вводит с клавиатуры три цифры.
# Необходимо создать число, содержащее эти цифры.
# Например, если с клавиатуры введено 1, 5, 7, тогда нужно сформировать число 157.

# Считываем три цифры с клавиатуры
digit1 = input("Введите первую цифру: ")
digit2 = input("Введите вторую цифру: ")
digit3 = input("Введите третью цифру: ")

# Создаем число из введенных цифр
number = int(digit1 + digit2 + digit3)

# Выводим результат на экран
print("Созданное число:", number)

