# Задание 2

# Пользователь вводит с клавиатуры три числа.
# Первое число — зарплата за месяц,
# второе число — сумма месячного платежа по кредиту в банке,
# третье число ­— задолженность за коммунальные услуги.
# Необходимо вывести на экран сумму, которая останется у пользователя после всех выплат.

# Считываем три числа с клавиатуры
salary = float(input("Введите зарплату за месяц: "))
credits = float(input("Введите сумму месячного платежа по кредиту: "))
payments = float(input("Введите задолженность за коммунальные услуги: "))

# Вычисляем остаток после выплат
balance = salary - credits - payments

# Выводим результат на экран
print("Остаток после всех выплат: ", balance)

