# Задание 1

# Пользователь вводит с клавиатуры три числа.
# Необходимо найти сумму чисел, произведение чисел.
# Результат вычислений вывести на экран.

# Считываем три числа с клавиатуры
a = float(input("Введите первое число: "))
b = float(input("Введите второе число: "))
c = float(input("Введите третье число: "))

# Вычисляем сумму чисел
sum = a + b + c

# Вычисляем произведение чисел
mult = a * b * c

# Выводим результаты на экран
print("Сумма чисел: ", sum)
print("Произведение чисел: ", mult)

