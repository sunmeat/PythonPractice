# Практика на переменные и типы данных 1 

# Пользователь вводит с клавиатуры два числа.
# Первое число – это значение, второе число процент, который необходимо посчитать.
# Например, мы ввели с клавиатуры 50 и 10.
# Требуется вывести на экран 10 процентов от 50.
# Результат: 5.

# Считываем два числа с клавиатуры
value = float(input("Введите значение: "))
percent = float(input("Введите процент: "))

# Вычисляем процент от значения
result = value * (percent / 100)

# Выводим результат на экран
print(f"{percent}% от {value} = {result}")

