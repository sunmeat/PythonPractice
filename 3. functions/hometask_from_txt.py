# Задание 1
# Напишите функцию, которая принимает два числа в качестве параметра и отображает
# все нечётные числа между ними.

def print_odd_numbers(start, end):
    for number in range(start, end + 1, 2):
        if number % 2:
            print(number, end = ' ')
    print()

