# Задание 5
# Напишите функцию, которая проверяет является ли число простым.
# Число передаётся в качестве параметра. Если число простое нужно вывести слово true, иначе false.

def is_prime(number):
    if number < 2:  # числа меньше 2 не являются простыми
        return False
    for i in range(2, int(number ** 0.5) + 1):
        if number % i == 0:
            return False
    return True

