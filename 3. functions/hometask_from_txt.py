# Задание 6
# Напишите функцию, которая проверяет является ли шестизначное число «счастливым».
# Число передаётся в качестве параметра. Если число счастливое, нужно вывести  true, иначе false.
# «Счастливое шестизначное число» — это число у которого сумма первых трёх цифр равна
# сумме трёх вторых цифр. Например, 123420 — счастливое 1+2+3 = 4+2+0, а 723422 — несчастливое 7+2+3 != 4+2+2.

def is_happy(number):
    if len(str(number)) != 6:  # проверка, что число является шестизначным
        return False

    digits = [int(digit) for digit in str(number)]  # преобразую число в список цифр
    left = sum(digits[:3])  # cумма первых трёх цифр
    right = sum(digits[3:])  # cумма последних трёх цифр

    return left == right

